package com.part1.myapplication.kotlin

fun main() {

    val numByte : Byte = 100
    val numShort : Short = 20
    val numInt = 1          // 자료형 생략 가능
    val numLong = 2L        // 자료형 생략 가능

    val numDouble : Double = 3.2
    val numFloat : Float = 3.2f

    println(numByte)
    println(numShort)
    println(numInt)
    println(numLong)
    println(numDouble)
    println(numFloat)

}